//! Runtime support for the `#[in_vm]` test macro.
//!
//! This crate implements the two outer tiers of the nested test environment:
//!
//! - **Host tier** ([`run_test_in_vm`]) -- launches a Docker container with the
//!   required devices and capabilities.
//! - **Container tier** ([`run_in_vm`] / [`TestVm`]) -- launches a VM via a
//!   pluggable [`HypervisorBackend`], monitors lifecycle events, and collects
//!   test output.
//!
//! The innermost tier (the VM guest init system) is provided by the `n-it`
//! crate.
//!
//! # Hypervisor backends
//!
//! The [`backend`] module defines the [`HypervisorBackend`] trait that
//! abstracts over different hypervisors.  Two implementations are
//! provided:
//!
//! - [`CloudHypervisor`] (in [`cloud_hypervisor`]) -- the default backend.
//! - [`Qemu`] (in [`qemu`]) -- QEMU/KVM backend using QMP for lifecycle
//!   control.
//!
//! [`TestVm`], [`run_in_vm`], and [`run_container_tier`] are all generic
//! over the backend.  The `#[in_vm]` proc macro selects the backend
//! based on an optional argument:
//!
//! - `#[in_vm]` -- uses [`CloudHypervisor`] (the default).
//! - `#[in_vm(cloud_hypervisor)]` -- same as above, explicitly.
//! - `#[in_vm(qemu)]` -- uses [`Qemu`].
//!
//! Optional companion attributes configure the VM:
//!
//! - `#[hypervisor(iommu)]` -- enable virtual IOMMU.
//! - `#[hypervisor(host_pages = "4k")]` -- 4 KiB host memory backing.
//! - `#[guest(hugepage_size = "2m", hugepage_count = 512)]` -- guest
//!   hugepage reservation.
//!
//! Callers can also substitute any backend that implements
//! [`HypervisorBackend`] by invoking the generic functions directly.
//!
//! # Error handling
//!
//! Both entry points return [`Result`] with dedicated error types
//! ([`VmError`] and [`ContainerError`]) so that callers can distinguish
//! infrastructure failures from test failures.
//!
//! # Protocol constants
//!
//! This crate re-exports all constants from [`n_vm_protocol`] so that code
//! generated by the `#[in_vm]` proc macro can reference them via `::n_vm::`
//! without requiring downstream crates to add `n-vm-protocol` as a direct
//! dependency.

